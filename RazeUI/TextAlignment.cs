namespace RazeUI
{
    public enum TextAlignment
    {
        /// <summary>
        /// The default alignment is Left for cultures that read from left to right, and Right for cultures
        /// that read right to left.
        /// </summary>
        Default,
        /// <summary>
        /// The text is aligned from left to right (flat left edge).
        /// </summary>
        Left,
        /// <summary>
        /// The text is aligned from right to left (flat right edge).
        /// </summary>
        Right,
        /// <summary>
        /// The text is aligned around the center (a column of text)
        /// </summary>
        Centered
    }
}
