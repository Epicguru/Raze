namespace RazeUI
{
    public enum Anchor
    {
        /// <summary>
        /// Places this UI element below the previous one, aligned to the left of it's container.
        /// </summary>
        Vertical,
        /// <summary>
        /// Places this UI element to the right of the previous one.
        /// </summary>
        Horizontal,
        /// <summary>
        /// Places this UI element below the previous one, trying to place this element centered relative to it's container.
        /// </summary>
        Centered
    }
}
